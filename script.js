// ===== SCROLL FADE ANIMATION (fade in saat masuk layar, fade out saat keluar) =====
document.addEventListener("DOMContentLoaded", () => {
    // Section besar (dari hero sampai footer) akan fade in/out saat discroll
    const fadeSections = document.querySelectorAll(
        "section#about, section#services, section#process, section#projects, section#experience, section#contact"
    );

    // Elemen kartu di dalam tiap section akan fade in dengan efek stagger (bertahap)
    const fadeItems = document.querySelectorAll(
        ".service-card, .process-card, .project-card, .certificate-card, .timeline-item, .tool-item, .stat-box"
    );

    fadeSections.forEach((el) => el.classList.add("scroll-fade"));
    fadeItems.forEach((el) => el.classList.add("scroll-fade-item"));

    const sectionObserver = new IntersectionObserver(
        (entries) => {
            entries.forEach((entry) => {
                if (entry.isIntersecting) {
                    entry.target.classList.add("is-visible");
                } else {
                    entry.target.classList.remove("is-visible");
                }
            });
        },
        { threshold: 0.15, rootMargin: "0px 0px -50px 0px" }
    );

    const itemObserver = new IntersectionObserver(
        (entries) => {
            entries.forEach((entry, i) => {
                if (entry.isIntersecting) {
                    // delay bertahap biar muncul satu-satu, bukan bareng semua
                    setTimeout(() => {
                        entry.target.classList.add("is-visible");
                    }, i * 80);
                } else {
                    entry.target.classList.remove("is-visible");
                }
            });
        },
        { threshold: 0.15, rootMargin: "0px 0px -30px 0px" }
    );

    fadeSections.forEach((el) => sectionObserver.observe(el));
    fadeItems.forEach((el) => itemObserver.observe(el));
});

// ===== HAMBURGER MENU (Navbar Mobile) =====
document.addEventListener("DOMContentLoaded", () => {
    const hamburgerBtn = document.getElementById("hamburgerBtn");
    const navLinkList = document.getElementById("navLinkList");
    const navOverlay = document.getElementById("navOverlay");

    if (hamburgerBtn && navLinkList && navOverlay) {
        const closeMenu = () => {
            hamburgerBtn.classList.remove("active");
            navLinkList.classList.remove("active");
            navOverlay.classList.remove("active");
            hamburgerBtn.setAttribute("aria-expanded", "false");
            document.body.classList.remove("freeze-layout");
        };

        const toggleMenu = () => {
            const isActive = navLinkList.classList.toggle("active");
            hamburgerBtn.classList.toggle("active", isActive);
            navOverlay.classList.toggle("active", isActive);
            hamburgerBtn.setAttribute("aria-expanded", isActive ? "true" : "false");
            document.body.classList.toggle("freeze-layout", isActive);
        };

        hamburgerBtn.addEventListener("click", toggleMenu);
        navOverlay.addEventListener("click", closeMenu);

        // Tutup menu otomatis saat salah satu link nav diklik
        navLinkList.querySelectorAll("a").forEach((link) => {
            link.addEventListener("click", closeMenu);
        });

        // Tutup menu kalau layar di-resize balik ke ukuran desktop
        window.addEventListener("resize", () => {
            if (window.innerWidth > 168) closeMenu();
        });
    }
});