function buildMetaItem(icon, label, value) {
    if (!value) return "";
    return `<div class="modal-meta-item">
                <span class="meta-label"><i class="fa-solid ${icon}"></i>${label}</span>
                <span class="meta-value">${value}</span>
            </div>`;
}

// Mengisi section detail opsional (Latar Belakang, Solusi, Hasil, dll).
// Aman dipanggil walau elemen-nya tidak ada di halaman (mis. modal di homepage
// yang lebih ringkas), karena selalu dicek dulu sebelum diakses.
function setOptionalSection(labelId, textId, value) {
    const label = document.getElementById(labelId);
    const el = document.getElementById(textId);
    if (!label || !el) return;

    if (value) {
        el.innerText = value;
        label.style.display = "block";
        el.style.display = "block";
    } else {
        el.innerText = "";
        label.style.display = "none";
        el.style.display = "none";
    }
}

function openProjectModal(id) {
    const modal = document.getElementById("projectModal");
    const data = projectData[id];

    if (data) {
        document.getElementById("modalImg").src = data.img;
        document.getElementById("modalImg").alt = data.title;
        document.getElementById("modalTitle").innerText = data.title;
        document.getElementById("modalDesc").innerText = data.desc;

        // Badge kategori di atas gambar
        const categoryBadge = document.getElementById("modalCategory");
        if (data.category) {
            categoryBadge.innerText = data.category;
            categoryBadge.style.display = "inline-block";
        } else {
            categoryBadge.style.display = "none";
        }

        // Grid info: peran, tahun, durasi, tools, platform, status
        const metaGrid = document.getElementById("modalMeta");
        const toolsValue = Array.isArray(data.tools) ? data.tools.join(", ") : data.tools;
        const metaHTML =
            buildMetaItem("fa-user-gear", "Peran", data.role) +
            buildMetaItem("fa-calendar-days", "Tahun", data.year) +
            buildMetaItem("fa-clock", "Durasi", data.duration) +
            buildMetaItem("fa-toolbox", "Tools", toolsValue) +
            buildMetaItem("fa-layer-group", "Platform", data.platform) +
            buildMetaItem("fa-circle-check", "Status", data.status);
        metaGrid.innerHTML = metaHTML;
        metaGrid.style.display = metaHTML ? "grid" : "none";

        // Latar belakang & tantangan proyek
        setOptionalSection("modalChallengeLabel", "modalChallenge", data.challenge);

        // Pendekatan & solusi yang diambil
        setOptionalSection("modalSolutionLabel", "modalSolution", data.solution);

        // Daftar fitur utama
        const featuresList = document.getElementById("modalFeatures");
        const featuresLabel = document.getElementById("modalFeaturesLabel");
        if (Array.isArray(data.features) && data.features.length > 0) {
            featuresList.innerHTML = data.features
                .map(f => `<li><i class="fa-solid fa-circle-check"></i><span>${f}</span></li>`)
                .join("");
            featuresLabel.style.display = "block";
        } else {
            featuresList.innerHTML = "";
            featuresLabel.style.display = "none";
        }

        // Proses pengerjaan tahap demi tahap (opsional, hanya ada di modal yang lebih detail)
        const processList = document.getElementById("modalProcess");
        const processLabel = document.getElementById("modalProcessLabel");
        if (processList && processLabel) {
            if (Array.isArray(data.process) && data.process.length > 0) {
                processList.innerHTML = data.process
                    .map((step, i) => `<li><span class="process-num">${i + 1}</span><span>${step}</span></li>`)
                    .join("");
                processLabel.style.display = "block";
                processList.style.display = "flex";
            } else {
                processList.innerHTML = "";
                processLabel.style.display = "none";
                processList.style.display = "none";
            }
        }

        // Hasil & pembelajaran dari proyek
        setOptionalSection("modalResultLabel", "modalResult", data.result);

        // Tag teknologi
        const tagsWrap = document.getElementById("modalTags");
        const tagSource = data.tags || data.tools;
        if (Array.isArray(tagSource) && tagSource.length > 0) {
            tagsWrap.innerHTML = tagSource.map(t => `<span>${t}</span>`).join("");
            tagsWrap.style.display = "flex";
        } else {
            tagsWrap.innerHTML = "";
            tagsWrap.style.display = "none";
        }

        // Tombol aksi (live demo & source code), otomatis sembunyi kalau linknya tidak diisi
        const ctaWrap = document.getElementById("modalCta");
        let ctaHTML = "";
        if (data.liveLink) {
            ctaHTML += `<a href="${data.liveLink}" target="_blank" class="cta-primary"><i class="fa-solid fa-arrow-up-right-from-square"></i>Lihat Live Demo</a>`;
        }
        if (data.githubLink) {
            ctaHTML += `<a href="${data.githubLink}" target="_blank" class="cta-secondary"><i class="fa-brands fa-github"></i>Lihat Source Code</a>`;
        }
        ctaWrap.innerHTML = ctaHTML;
        ctaWrap.style.display = ctaHTML ? "flex" : "none";

        modal.style.display = "flex";
    }
}

